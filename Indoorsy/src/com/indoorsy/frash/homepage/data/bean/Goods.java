package com.indoorsy.frash.homepage.data.bean;

import java.io.Serializable;

/**
 * 特价产品类
 * 
 * @author gq
 *
 */
public class Goods implements Serializable {

	private static final long serialVersionUID = 1L;

	private int goodsid;
	private String bargain;
	private String goodsCurrentPrices;
	private String goodsDesc;
	private String goodsName;
	private String goodsSales;// 已售
	private String goodsThumb;
	private String punitPrice;
	private String unitTitle;

	/**
	 * 获取特价产品id
	 * 
	 * @return
	 */
	public int getGoodsid() {
		return goodsid;
	}

	public void setGoodsid(int goodsid) {
		this.goodsid = goodsid;
	}

	/**
	 * 获取特价产品特价价格
	 * 
	 * @return
	 */
	public String getBargain() {
		return bargain;
	}

	public void setBargain(String bargain) {
		this.bargain = bargain;
	}

	/**
	 * 判断特价产品是否 [时价]---"1"显示时价---"0"不显示---
	 * 
	 * @return
	 */
	public String getGoodsCurrentPrices() {
		return goodsCurrentPrices;
	}

	public void setGoodsCurrentPrices(String goodsCurrentPrices) {
		this.goodsCurrentPrices = goodsCurrentPrices;
	}

	/**
	 * 获取特价产品描述
	 * 
	 * @return
	 */
	public String getGoodsDesc() {
		return goodsDesc;
	}

	public void setGoodsDesc(String goodsDesc) {
		this.goodsDesc = goodsDesc;
	}

	/**
	 * 获取特价产品名称
	 * 
	 * @return
	 */
	public String getGoodsName() {
		return goodsName;
	}

	public void setGoodsName(String goodsName) {
		this.goodsName = goodsName;
	}

	/**
	 * 获取特价产品已售
	 * 
	 * @return
	 */
	public String getGoodsSales() {
		return goodsSales;
	}

	public void setGoodsSales(String goodsSales) {
		this.goodsSales = goodsSales;
	}

	/**
	 * 获取特价产品图片
	 * 
	 * @return
	 */
	public String getGoodsThumb() {
		return goodsThumb;
	}

	public void setGoodsThumb(String goodsThumb) {
		this.goodsThumb = goodsThumb;
	}

	/**
	 * 获取特价产品原价格
	 * 
	 * @return
	 */
	public String getPunitPrice() {
		return punitPrice;
	}

	public void setPunitPrice(String punitPrice) {
		this.punitPrice = punitPrice;
	}

	/**
	 * 获取特价产品单位
	 * 
	 * @return
	 */
	public String getUnitTitle() {
		return unitTitle;
	}

	public void setUnitTitle(String unitTitle) {
		this.unitTitle = unitTitle;
	}

}
