package com.indoorsy.frash.homepage.data.bean;

import java.io.Serializable;

/**
 * 快递、配送时间
 * 
 * @author
 * 
 */
public class DetailLictdl implements Serializable {

	private static final long serialVersionUID = 1L;

	private String delContent;
	private String deltime;
	private String deliverid;
	
	/**
	 * 获取 配送方式id
	 * 
	 * @return
	 */
	public String getDeliverid() {
		return deliverid;
	}

	public void setDeliverid(String deliverid) {
		this.deliverid = deliverid;
	}

	/**
	 * 获取配送方式：快递
	 * 
	 * @return
	 */
	public String getDelContent() {
		return delContent;
	}

	public void setDelContent(String delContent) {
		this.delContent = delContent;
	}

	/**
	 * 获取配送时间：每天中午10:00
	 * 
	 * @return
	 */
	public String getDeltime() {
		return deltime;
	}

	public void setDeltime(String deltime) {
		this.deltime = deltime;
	}

}
