package com.indoorsy.frash.util;

public class TypeUtil {
	public static int byShowTextGetId(String showText){
		int result = 1;
		if("综合排序".equals(showText)){
			result = 1;
		}else if("最新上线".equals(showText)){
			result = 2;
		}else if("销量".equals(showText)){
			result = 3;
		}else if("好评".equals(showText)){
			result = 4;
		}else if("价格从高到低".equals(showText)){
			result = 5;
		}else if("价格从低到高".equals(showText)){
			result = 6;
		}
		return result;
	}
}
